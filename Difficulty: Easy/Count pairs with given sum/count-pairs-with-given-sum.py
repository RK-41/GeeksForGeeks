class Solution:
    #Complete the below function
    def countPairs(self,arr, target):
        #Your code here
        # 27.12.24 POTD
        ans = 0
        fr = {}
        
        for a in arr:
            diff = target - a            
            ans += fr.get(diff, 0)
            fr[a] = fr.get(a, 0) + 1

        return ans

