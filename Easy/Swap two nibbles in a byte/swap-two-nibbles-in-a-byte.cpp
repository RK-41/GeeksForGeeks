class Solution {
  public:
    int swapNibbles(int n) {
        // code here
        // 31.05.24 POTD
        int ans=0;
        
        for(int i=0; i<4; i++){
            ans += ((n&(1<<i))<<4);
            ans += ((n&(1<<(i+4)))>>4);
        }
        
        return ans;
    }
};

