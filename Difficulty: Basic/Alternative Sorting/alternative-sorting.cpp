class Solution {

  public:
    vector<int> alternateSort(vector<int>& arr) {
        // Your code goes here
        // 25.10.24 POTD
        vector<int> ans, v = arr;
        int i=0, j=arr.size()-1;
        
        sort(v.begin(), v.end());
        while(i<=j){
            ans.push_back(v[j--]);
            if(i<=j) ans.push_back(v[i++]);
        }
        
        return ans;
    }
};


